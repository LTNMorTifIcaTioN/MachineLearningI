# Multilayer Perceptron
"""
As redes neurais Perceptron de múltiplas camadas podem ser abstraídas em quatro principais etapas; são elas:
(1) inicialização, em que valores aleatórios são atribuídos aos pesos e limites;
(2) ativação, em que é realizado o cálculo dos valores dos neurônios das camadas ocultas e dos valores da camada de saída;
(3) treinamento dos pesos, em que é calculada a taxa de erro e aplicada a correção e a atualização dos pesos;
(4) iteração, em que o processo é repetido até que o critério de erro definido na inicialização seja cumprido;
"""

